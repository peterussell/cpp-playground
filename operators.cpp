#include <iostream>

using namespace std;

int main()
{
  // Demonstrates the basic arithmetic operators
  int m = 54;
  int n = 20;

  cout << "m + n = " << m+n << endl;
  cout << "m - n = " << m-n << endl;
  cout << "m * n = " << m*n << endl;
  cout << "m / n = " << m/n << endl;
  cout << "m % n = " << m%n << endl;

  cout << endl;
}
